const FishTexture f; const BirdTexture b; const DogTexture d; const RabbitTexture r;

class Animation {
private:
	string type;
public:
	Texture frames;

	Animation() {};

	int apply_animation(string t) {
		this->type = t;
		if (t == types_of_animals[0]) {
			this->frames.frame_1 = f.frame_1;
			this->frames.frame_2 = f.frame_2;
			this->frames.frame_idle = f.frame_idle;
		}
		if (t == types_of_animals[1]) {
			this->frames.frame_1 = d.frame_1;
			this->frames.frame_2 = d.frame_2;
			this->frames.frame_idle = d.frame_idle;
		}
		if (t == types_of_animals[2]) {
			this->frames.frame_1 = b.frame_1;
			this->frames.frame_2 = b.frame_2;
			this->frames.frame_idle = b.frame_idle;
		}
		if (t == types_of_animals[3]) {
			this->frames.frame_1 = r.frame_1;
			this->frames.frame_2 = r.frame_2;
			this->frames.frame_idle = r.frame_idle;
		}
		return 0;
	}
};